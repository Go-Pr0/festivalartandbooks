/**
 * Build fields array for a site page YAML file.
 * @param {string} slug
 * @param {{ seoTitle?: boolean, hero?: boolean, stats?: boolean, timeline?: boolean, trustPoints?: boolean, dealsIn?: boolean, figures?: boolean }} sections
 */
function pageFields(slug, sections = {}) {
  /** @type {DecapCollection[]} */
  const fields = [];

  fields.push(seoFields({ titleRequired: sections.seoTitle ?? true }));
  if (sections.hero) fields.push(...heroFields());
  if (sections.stats) fields.push(statsField());
  if (sections.timeline) fields.push(timelineField());
  if (sections.trustPoints) fields.push(trustPointsField());
  if (sections.dealsIn) fields.push(dealsInField());
  if (sections.figures) fields.push(figuresField());
  fields.push(textSection(PAGE_TEXT[slug]));

  return fields;
}

/** @param {Array<Record<string, unknown>>} files */
function pageFilesCollection(name, label, files) {
  return {
    name,
    label,
    icon: 'web',
    files,
  };
}

